    private void button2ActionPerformed(java.awt.event.ActionEvent evt) {                                        
        try{
            float perc=Float.parseFloat(T6.getText());
            if(perc>=90){
                T7.setText("A");
            }
            else if(perc>=70){
                T7.setText("B");
            }
            else if(perc>=60){
                T7.setText("C");    
            }
            else if(perc>=33){
                T7.setText("D");
            }
            else{
                T7.setText("F");
            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,"INPUT ALL THE FIELDS!!!");
        }
    }                                       

