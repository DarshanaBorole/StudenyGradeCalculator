    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {                                        
        try{
            int ds,cs,nlp,ann;
            ds=Integer.parseInt(T1.getText());
            cs=Integer.parseInt(T2.getText());
            nlp=Integer.parseInt(T3.getText());
            ann=Integer.parseInt(T4.getText());
            int total=ds+cs+nlp+ann;
            if(r1.isSelected()==true){
                total=total+5;
            }
            T5.setText(""+total);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,"INPUT ALL THE FIELDS!!!");
        }
    }                                       

